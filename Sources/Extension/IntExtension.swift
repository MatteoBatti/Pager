import UIKit


extension Int {
    
    var float: Float {
        get {
            return Float(self)
        }
    }
    
    var double: Double {
        get {
            return Double(self)
        }
    }
    
    var cgFloat: CGFloat {
        get {
            return CGFloat(self)
        }
    }
    
}

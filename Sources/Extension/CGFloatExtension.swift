import UIKit


extension CGFloat {
    
    var float: Float {
        get {
            return Float(self)
        }
    }
    
    var double: Double {
        get {
            return Double(self)
        }
    }
    
    var int: Int {
        get {
            return Int(self)
        }
    }
    
}
